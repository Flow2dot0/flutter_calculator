class Operations{

  static pourcentage({num nb}) => nb/100;
  static divide({num nbLeft, num nbRight}) => nbLeft / nbRight;
  static multiply({num nbLeft, num nbRight}) => nbLeft * nbRight;
  static substract({num nbLeft, num nbRight}) => nbLeft - nbRight;
  static addition({num nbLeft, num nbRight}) => nbLeft + nbRight;

}